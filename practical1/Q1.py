# 1.	Write a Python program to create a list and a tuple, and demonstrate the difference between them.
# Creating a list
my_list = [1, 2, 3, 4, 5]
# Creating a tuple
my_tuple = (1, 2, 3, 4, 5)
# Demonstrating the difference
print("List:", my_list)
print("Tuple:", my_tuple)
print("Type of my_list:", type(my_list))
print("Type of my_tuple:", type(my_tuple))
